function populate_records($keyword='', $page, $limit, $order, $sort) {
  global $DB;
  $startpoint = $limit * ($page - 1);
	$search = '';
	
	$query = $DB->Fetch('forecasts_all_plan_view', array(
							'columns'	=> '*',
					    'order' 	=> $order .' '.$sort,
    					'limit'		=> $startpoint .', '.$limit,
             )
           );
	return array("forecasts_all_plan" => $query, "total" => $DB->totalRows());
}
